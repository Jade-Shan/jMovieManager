    /**
    * Setzt die Grafik und die URL, um eine Spende zu tÃ¤tigen
    */
    private void setLocalizedDonation(){
        Locale currentLocale = LocaleManager.getInstance().getCurrentLocale();
        
        if(currentLocale.getLanguage().equals(LocaleManager.SupportedLanguages.de_DE.getLocale().getLanguage())){
            jButton2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/jMovieManager/swing/images/btn_donateCC_LG_DE.gif")));
            payPalURL ="https://www.paypal.com/cgi-bin/webscr?cmd=_s-xclick&hosted_button_id=3NDAD63S843MN";        //DE + â¬
        }
        else{
            jButton2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/jMovieManager/swing/images/btn_donateCC_LG_US.gif")));
            payPalURL = "https://www.paypal.com/cgi-bin/webscr?cmd=_s-xclick&hosted_button_id=LX8YKRZMBS2RN";       //USA + $
        }
    }
            
