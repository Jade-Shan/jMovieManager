    private void jTextField8KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField8KeyReleased
        if(!(evt.getKeyChar() == KeyEvent.VK_BACK_SPACE) || (evt.getKeyChar() == KeyEvent.VK_DELETE) || (evt.getKeyChar() == KeyEvent.VK_SPACE) || (evt.getKeyChar() == KeyEvent.VK_COMMA)){
            //Suchanfrage
            int index = jTextField8.getText().lastIndexOf(", ");
            String actorToSearch;
            if(index > -1){
                actorToSearch = jTextField8.getText().substring(index+2);
            }
            else{
                actorToSearch = jTextField8.getText();
            }
            if((!actorToSearch.isEmpty()) && (actorToSearch.length() > 2)){
                for(Actor actor: Actor.getActorsSet()){
                    if(actor.getName().startsWith(actorToSearch)){
                        int oldSize = jTextField8.getText().length();
                        jTextField8.setText(jTextField8.getText() + actor.getName().substring(actorToSearch.length()) + ", ");
                        jTextField8.select(oldSize, jTextField8.getText().length());                   
                        break;
                    }
                }   
            }
        }
    }//GEN-LAST:event_jTextField8KeyReleased

