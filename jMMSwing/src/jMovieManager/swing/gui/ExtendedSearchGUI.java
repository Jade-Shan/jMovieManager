    /**
     * Updates the content of the collection combo box
     */
    private void updateCollectionComboBox(){
        collectionComboBoxModel.removeAllElements();
        collectionComboBoxModel.addElement(bundle.getString("ExtendedSearchGUI.jComboBox1.entry"));
        for(MediaCollection value: MediaCollection.getCollectionMap().values()){
            if(((searchType.equals(MediaType.MOVIE) && value instanceof MovieCollection)) ||
                    ((searchType.equals(MediaType.SERIE) && value instanceof SerieCollection))) {
                collectionComboBoxModel.addElement(value);
           }
        }
        collectionComboBoxModel.setSelectedItem(bundle.getString("ExtendedSearchGUI.jComboBox1.entry"));
    }
        
    /**
     * Methode welche dem Jlist1Model Genres, als auch die zugehörigen Einträge der HashMap hinzufügt
     */
    private void addGenresToModel(){   
        for(String genreKey: GENRE_KEYS){
            //Füge die Genres einer Hashmap hinzu, damit nach Auswahl nicht Lokalisierte Wert gespeichert werden kann 
            if(genreKeyAndCodecBundle.containsKey(genreKey)){
                genresMap.put(genreKeyAndCodecBundle.getString(genreKey), genreKey);
                genresListModel.addElement(genreKeyAndCodecBundle.getString(genreKey));
            }
        }
    }
    
