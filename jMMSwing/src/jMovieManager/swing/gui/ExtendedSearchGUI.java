    /**
     * 
     * Macht die GUI sichtbar und gibt die gefilterten Elemente zurück
     * 
     * @return Liste der gefilterten Movie Elemente
     */
    public MediaCollection<? extends MediaFile> showFilterGUI(){
        this.setVisible(true);    
                
        if(canceled){
            canceled = false;
            return null;
        }
        
        //Filtere Datenbestand
        ExtendedSearch filter = ExtendedSearch.getInstance();
        
        if(searchType == MediaType.MOVIE){
        filter.filterMovies(
                bundle.getString("ExtendedSearchGUI.tabName"), //collection title
                (collection != null)? (MovieCollection)collection: null, //Collection
                title,                       //Titel
                oTitle,                      //oTitel
                genreKeys,                   //Genres
                playtimeFrom,                //PlaytimeFrom
                playtimeTo,                  //PlaytimeTo
                rlsYearFrom,                 //RlsDateFrom
                rlsYearTo,                   //RlsDateTo
                widthFrom,
                heightFrom,
                widthTo,
                heightTo,
                publisher,                   //Publisher
                actors,                      //Actors
                director,                    //Director  
                imdbRatingFrom,              //ImdbRatingFrom
                imdbRatingTo,                //ImdbRatingTo
                persRatingFrom,              //PersRatingFrom
                persRatingTo,                //PersRatingTo
                fskList,
                watched);
        }
        else if(searchType == MediaType.SERIE){
        filter.filterSeries(
                bundle.getString("ExtendedSearchGUI.tabName"), //collection title
                (collection != null)? (SerieCollection)collection: null,
                title,                       //Titel
                oTitle,                      //oTitel
                genreKeys,                   //Genres
                playtimeFrom,                //PlaytimeFrom
                playtimeTo,                  //PlaytimeTo
                rlsYearFrom,                 //RlsDateFrom
                rlsYearTo,                   //RlsDateTo
                publisher,                   //Publisher
                actors,                      //Actors
                director,                    //Director  
                imdbRatingFrom,              //ImdbRatingFrom
                imdbRatingTo,                //ImdbRatingTo
                persRatingFrom,              //PersRatingFrom
                persRatingTo,                //PersRatingTo
                fskList);
        }
        //Gebe die gefilterten Filmelemente zurück
        return filter.getSearchCollection();
    }    
    
