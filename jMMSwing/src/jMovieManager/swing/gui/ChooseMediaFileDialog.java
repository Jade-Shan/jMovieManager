  /**
    * Inserts the search results into the table model 
    */
    private void insertSimilarTitles(){
        for(T result : searchResults){
            if(!result.getTitle().isEmpty()){
                String rlsYearString;
                if(result.getReleaseYear() == 0){
                    rlsYearString = "";
                }else{
                    rlsYearString = String.valueOf(result.getReleaseYear());
                }
                
                if(result instanceof TheTVDBSeries){                  
                    tableModel.addRow(new Object[]{result.getImagePath(), result.getTitle(),
                        rlsYearString, bundle.getString("ChooseMediaFileDialogGUI.series")});  
                }
                else{      
                    tableModel.addRow(new Object[]{result.getImagePath(), result.getTitle(),
                        rlsYearString, bundle.getString("ChooseMediaFileDialogGUI.movie")});  
                }
            }
        }
    }

    /**
    * Shows the GUI and returns the selected result.
    * @return The selected result <br/> null, if the dialog was canceled
    **/
    public T showDialog(){
        this.setVisible(true);
        //Wird erst nach dem Klick auf OK ausgefÃ¼hrt:
        if(!isCanceled()){
            int index = jTable1.getSelectedRow();
            if(index > -1){
                return searchResults.get(index);
            }
        }
        return null;
    }
    
